// ASSIGNMENT OPERATOR
let r = 5;

//ADDITION ASSIGNMENT OPERATOR
r += 1; // r = r + 1
console.log(r);

//SUBTRACTION ASSIGNMENT OPERATOR
r -= 1; // r = r - 1

//MULTIPLICATION ASSIGNMENT OPERATOR
r *= 1; //r = r * 1

// DIVISION ASSIGNMENT OPERATOR
r /= 1; // r = r / 1

// REMAINDER (or module) ASSIGNMENT
r %= 1; // r = r % 1

// EXPONENTIAL ASSIGNMENT
r **= 1; //r = r ** 1

